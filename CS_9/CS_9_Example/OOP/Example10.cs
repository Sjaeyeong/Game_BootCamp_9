▶ 객체지향 5대 설계 원칙

1. SRP (단일 책임의 원칙)

- 하나의 클래스는 하나의 책임만 가진다.
 ㄴ 변경 이유는 하나여야 한다.
- 결합도는 낮추고 응집도를 높여라.


★★★★★
결합도
 ㄴ 모듈(클래스)간의 상호 의존 정도를 나타내는 지표
 ㄴ 결홉다고 낮으면 상호 의존성이 줄어서 재사용 및 유지보수가 유리하다.

★★★★★
응집도
 ㄴ 하나의 클래스 내부에 존재하는 구성 요소들의 기능적 관련성
 ㄴ 응집도가 높은 모듈은 하나의 책임에 집중하고 독립성이 높아진다. → 재사용 + 유지보수에 유리


2. OCP (개방 폐쇄의 원칙)

- 자신의 확장에는 열려 있고 주변의 변화에 대해서는 닫혀 있어야 한다.
 ㄴ 기존 코드를 수정하지 않고 기능을 확장할 수 있어야 함.

- 추상 클래스 / 인터페이스 활용


3. LSP (리스코브 치환의 원칙)

- 하위 타입은 언제나 자신의 기반인 상위 타입으로 교체할 수 있어야 한다.
 ㄴ 자식 클래스는 언제나 부모 클래스를 대체할 수 있어야 한다.

- 부모 타입으로 사용해도 프로그램의 의미가 꺠지지 않아야 함.

EX : 상위 → 운송 수단 / 자동차 → 하위 → 테슬라, 현대


4. ISP (인터페이스 분리의 원칙)

- 하나의 거대한 인터페이스보다 여러 개의 작은 인터페이스가 좋다.

- 사용하지 않는 기능에 의존하지 않도록 설계

EX : 스타크래프트 → 종족 카테고리
 ㄴ 추상화 → 플 / 저 / 테


5. DIP (의존관계 역전의 법칙)

- 고수준 모듈은 저수준 모듈에 의존하지 않는다.

- 필요한 경우 추상화에 의존한다.
 ㄴ 구현이 아닌 인터페이스에 의존


● 객체설계 5원칙 요약

(S) 단일 책임 원칙 : 객체는 오직 하나의 책임을 가져야 한다.
(O) 개방 폐쇄 원칙 : 객체는 확장에 대해서는 개방적 / 수정에 대해서는 폐쇄적이여야 한다.
(L) 리스코브 치환 원칙 : 자식 클래스는 언제나 자신의 부모 클래스를 대체할 수 있어야 한다.
(I) 인터페이스 분리 원칙 : 인터페이스는 작은 단위들로 분리 / 사용하지 않는 기능들은 포함하지 말아야 한다.
(D) 의존성 역전 원칙 : 추상성이 높고 안정적인 고수준 클래스는 구체적이고 불안정한 저수준의 클래스에 의존하지 않아야 한다.

*/
