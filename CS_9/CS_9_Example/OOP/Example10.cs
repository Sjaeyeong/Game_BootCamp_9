● 객체지향 4대 특징

1. 캡슐화

- 객체 내부 상태를 외부로부터 보호
- 허용된 방법으로만 접근 가능
- 데이터 무결성과 안정성 확보

EX : 자동차 → 시동 어떻게 걸까요..? (브레이크 + 버튼 / 브레이크 + 키를 돌리든 / 어플)


2. 상속

- 기존 클래스를 기반으로 새로운 클래스를 확장 (공통 기능 재사용)
- 객체지향에서의 상속은 하위로 내려갈 수록 구체화 되는것을 의미
 ㄴ 상속 클래스 → 하위 클래스 구조 / 2 / 3
 ㄴ 일반적으로 부모 → 자식 관계 형성

- 프로그램 구조에 대한 이해도 향상

- 재사용성 향상

- 확장성 향상
 ㄴ 양날의 검 같은 느낌

- 유지보수성 향상


3. 다형성

- 하나의 인터페이스 또는 부모 타입이 여러 형태의 객체로 동작할 수 있는 성질을 뜻한다.

- C# 핵심 요소
 ㄴ virtual / override
 ㄴ interface 구현
 ㄴ 부모 타입 참조로 자식 객체 사용

EX : 
부모 타입 Character
 ㄴ 다형성을 통해 → Player / Monster를 만들어 낼 수 있을것이며 다형성을 또 이용하면 각자 다른 공격 방식 구현


4. 추상화

- 공통적인 부분과 특정 특성을 분리 및 추출해 재조합 및 재구성

- 불필요한 세부 구현을 숨기고 필요한 기능만 노출

- 클래스를 정의할 당시 구체화 시킬 수 없는 기능을 추상적 표현으로 정의


