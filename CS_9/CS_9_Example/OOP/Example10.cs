▶ 객체지향 프로그래밍

- 객체지향 프로그래밍은 프로그램을 설계하는 방법론이자 개념

- 프로그램을 독립적인 객체들의 상호작용으로 구성하는 방식

- 각 객체는 자신의 데이터와 기능을 함께 가진다. (상태 + 행위)

- OOP는 코드의 재사용성과 유지보수성이 높고 → 이러한 특성때문에 대규모 프로그램에 적합하다.

- 4대 특징 + 5대 설계원칙을 지키면 안정적이고 신뢰성 있는 프로그램 설계를 가능하게 한다.


● 절차지향과 객체지향

- 절차지향
 ㄴ 프로그램의 실행 순서와 흐름을 중심으로 설계
 ㄴ 구조적 (함수) 순서가 매우 중요 → 함수 호출 순서가 중요하다.
 ㄴ 소규모에 적합


- 객체 지향
 ㄴ 객체간의 역할과 책임을 중심으로 설계한다,.
 ㄴ 객체간 협력이 핵심
 ㄴ 중/대규모 프로그램에 적합하다.

※ 장점
1. 객체 단위 → 관리가 쉽고 유지보수에 용이하다.
2. 4대 특징등을 이용해 코드 재사용성 향상
3. 역할이 분리된 설계로 생산성 향상
4. 변경에 강한 구조 설계를 가능하게 한다.


※ 단점
1. 설계 단계에서 많은 고민과 시간이 필요 → 프로젝트
2. 과도한 추상화는 오리혈 복잡도를 증가 시킨다.
